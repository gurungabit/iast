def get_path_by_office(office: str) -> str | None:
    """Get file path by office code"""
    for entry in FILE_PATHS:
        if entry["office"] == office:
            return entry["path"]
    return None


def get_path_by_zone(zone: str) -> list[str]:
    """Get all file paths for a given zone"""
    return [entry["path"] for entry in FILE_PATHS if entry["zone"] == zone]


def get_zone_by_office(office: str) -> str | None:
    """Get zone name by office code"""
    for entry in FILE_PATHS:
        if entry["office"] == office:
            return entry["zone"]
    return None


def build_file_path(office: str, department: str, filename: str) -> str:
    """
    Build complete file path given office code, department, and filename.

    Args:
        office: Office code (e.g., "01", "02", "15")
        department: Department name ("FIRE" or "AUTO")
        filename: Name of the file

    Returns:
        Complete UNC path to the file

    Raises:
        ValueError: If office code not found or invalid department

    Example:
        >>> build_file_path("01", "AUTO", "RW1AA271.accdb")
        '\\\\Opr.statefarm.org\\dfs\\ZONE\\GREAT LAKES\\WORKGROUP\\iAST\\AUTO\\RW1AA271.accdb'
    """
    # Validate department
    department = department.upper()
    if department not in ["FIRE", "AUTO"]:
        raise ValueError(f"Invalid department: {department}. Must be 'FIRE' or 'AUTO'")

    # Get base path for office
    base_path = get_path_by_office(office)
    if base_path is None:
        raise ValueError(f"Office code '{office}' not found")

    # Remove trailing backslash if present
    base_path = base_path.rstrip("\\")

    # Build complete path
    return f"{base_path}\\{department}\\{filename}"
